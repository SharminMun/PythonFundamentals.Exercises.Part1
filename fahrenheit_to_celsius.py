
# Declare a variable called fahrenheit.
fahrenheit = 32

# Convert the value of fahrenheit to celsius.
celsius = (fahrenheit - 32) * 5 / 9

# conversion should be displayed on the screeen in the following format:
# "32 degrees fahrenheit is equal to 0.0 degree(s) celsius"

print(f"{fahrenheit} degrees Fahrenheit is equal to {celsius:.1f} degree(s) Celsius")